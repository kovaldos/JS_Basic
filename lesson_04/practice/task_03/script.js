// 1. С помощью цикла for выведите в консоль числа от 11 до 33.
const loop1 = () => {
    for (let i = 11; i <= 33; i++) {
        console.log(i);
    }
}

// loop1();

// 2. С помощью цикла for выведите в консоль нечетные числа в
// промежутке от 1 до 99.

const loop2 = () => {
    for (let i = 1; i <= 99; i++) {
        if (i % 2 !== 0) {
            console.log(i);
        }
    }
}

// loop2();

// 3. С помощью цикла for выведите в консоль числа от 100 до 0.

const loop3 = () => {
    for (let i = 100; i >= 0; i--) {
        console.log(i);
    }
}

