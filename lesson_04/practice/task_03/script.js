// 4. Дано число num с неким начальным значением. Умножайте его на 3
// столько раз, пока результат умножения не станет больше 1000.
// Какое число получится? Посчитайте количество итераций, необходимых для этого.

let num = 3;
let iterations = 0;

while (num <= 1000) {
    num *= 3;
    iterations++;
}

console.log("Итоговое число:", num);
console.log("Количество итераций:", iterations);



